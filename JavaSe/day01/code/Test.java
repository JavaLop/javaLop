/*
1. public   公共的  它就是一个修饰符
2. class    类
3. Test     类名(这个类名必须与文件相同)
*/
public class Test{
    /*
        static  静态的(它就是一个修饰符)
        void    无返回值
        main    方法名，也是java程序的入口，主方法，主线程
        String  字符串，    String[] 字符串数组
        args    变量名 -----是可以修改的
    */
    public static void main(String[] args){
        System.out.println("Hello World!");
    }
}
